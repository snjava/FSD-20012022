public class Operator4
{
	public static void main(String ar[])
	{
		int a = 5;
		int b = 7;
		System.out.println(a & b); // 5	
		System.out.println(a | b); // 7	 
		System.out.println(true & false); // false
		System.out.println((a>10) & (b<10)); // false & true = false
	}
}

