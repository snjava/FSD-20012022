public class VariableDemo5
{
	public static void main(String ar[])
	{
		int octal = 027;
		int binary = 0b1010;
		int decimal = 27;
		int hexa = 0Xcafe;

		System.out.println(octal);  // 23
		System.out.println(binary); // 10
		System.out.println(decimal); // 27
		System.out.println(hexa); // 10
	}
}