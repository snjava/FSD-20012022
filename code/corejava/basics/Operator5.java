public class Operator5
{
	public static void main(String ar[])
	{
		
		int a = 10;
		int b = 20;
		System.out.println((a>10) & (b<=20));  // False & True  = False
		System.out.println((a>10) && (b<=20)); // False
		int x = 20;
		System.out.println(x << 2); // left Shift = 80
		System.out.println(x >> 2); // right Shift = 5	

	}
}











