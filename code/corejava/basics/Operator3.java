public class Operator3
{
	public static void main(String ar[])
	{
		int x = 10;
		int y = 20;
		System.out.println(x > y); // false
		System.out.println(x > 10); // false
		System.out.println(x >= 10); // true
		System.out.println(x == 10); // true
		System.out.println(x != 10); // false
	}
}











